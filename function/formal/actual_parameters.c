// Formal and Actual Parameters
// Formal Parameters: The parameters which are used in the function definition are called formal parameters.
// Actual Parameters: The parameters which are used in the function call are called actual parameters.
// Example:
// #include<stdio.h>
//
//     void sum(int a, int b);
//     int main(){
//     sum(1, 2);
//     return 0;
// }
//
// void sum(int a, int b){
//     int c = a + b;
//     printf("%d", c);
// }

// Here a and b are formal parameters and 1 and 2 are actual parameters.
// Formal parameters are also called as formal arguments and actual parameters are also called as actual arguments. 
// Formal parameters are used in the function definition and actual parameters are used in the function call.
// Formal parameters are declared in the function definition and actual parameters are declared in the function call.
// Formal parameters are initialized with the value of actual parameters.
// Formal parameters are used to access the arguments passed in the function call.
// Formal parameters are local to the function and are destroyed when the function returns.
// Actual parameters are used to pass the arguments in the function call.
// Actual parameters are used to initialize the formal parameters.
// Actual parameters are used to pass the values to the function.
// Actual parameters are destroyed when the function returns.


